package bitcamp.java100.ch06.ex4;

public class Calculator3 {
    int result;

    void plus(int value) {
        this.result += value;
    }

    void minus(int value) {
        this.result -= value;
    }

    void multiple(int value) {
        this.result *= value;
    }

    void divide(int value) {
        this.result /= value;
    }
}
